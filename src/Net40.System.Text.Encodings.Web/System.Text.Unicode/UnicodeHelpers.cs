	[MethodImpl(MethodImplOptionsEx.AggressiveInlining)]
	internal static int GetScalarValueFromUtf16(char first, char? second, out bool wasSurrogatePair, out bool needsMoreData)
	{
		if (!char.IsSurrogate(first))
		{
			wasSurrogatePair = false;
			needsMoreData = false;
			return first;
		}
		return GetScalarValueFromUtf16Slow(first, second, out wasSurrogatePair, out needsMoreData);
	}

	private static int GetScalarValueFromUtf16Slow(char first, char? second, out bool wasSurrogatePair, out bool needMoreData)
	{
		if (!char.IsSurrogate(first))
		{
			Debug.Assert(condition: false, "This case should've been handled by the fast path.");
			wasSurrogatePair = false;
			needMoreData = false;
			return first;
		}
		if (char.IsHighSurrogate(first))
		{
			if (second.HasValue)
			{
				if (char.IsLowSurrogate(second.Value))
				{
					wasSurrogatePair = true;
					needMoreData = false;
					return GetScalarValueFromUtf16SurrogatePair(first, second.Value);
				}
				wasSurrogatePair = false;
				needMoreData = false;
				return 65533;
			}
			wasSurrogatePair = false;
			needMoreData = true;
			return 65533;
		}
		Debug.Assert(char.IsLowSurrogate(first));
		wasSurrogatePair = false;
		needMoreData = false;
		return 65533;
	}

	[MethodImpl(MethodImplOptionsEx.AggressiveInlining)]
	internal static unsafe int GetScalarValueFromUtf16(char* pChar, bool endOfString)
	{
		char thisChar = *pChar;
		return char.IsSurrogate(thisChar) ? GetScalarValueFromUtf16Slow(pChar, endOfString) : thisChar;
	}

	private static unsafe int GetScalarValueFromUtf16Slow(char* pChar, bool endOfString)
	{
		char firstChar = *pChar;
		if (!char.IsSurrogate(firstChar))
		{
			Debug.Assert(condition: false, "This case should've been handled by the fast path.");
			return firstChar;
		}
		if (char.IsHighSurrogate(firstChar))
		{
			if (endOfString)
			{
				return 65533;
			}
			char secondChar = pChar[1];
			if (char.IsLowSurrogate(secondChar))
			{
				return GetScalarValueFromUtf16SurrogatePair(firstChar, secondChar);
			}
			return 65533;
		}
		Debug.Assert(char.IsLowSurrogate(firstChar));
		return 65533;
	}

	private static int GetScalarValueFromUtf16SurrogatePair(char highSurrogate, char lowSurrogate)
	{
		Debug.Assert(char.IsHighSurrogate(highSurrogate));
		Debug.Assert(char.IsLowSurrogate(lowSurrogate));
		return (lowSurrogate & 0x3FF) | ((highSurrogate & 0x3FF) + 64 << 10);
	}

	internal static void GetUtf16SurrogatePairFromAstralScalarValue(int scalar, out char highSurrogate, out char lowSurrogate)
	{
		Debug.Assert(65536 <= scalar && scalar <= 1114111);
		int x = scalar & 0xFFFF;
		int u = scalar >> 16;
		int w = u - 1;
		highSurrogate = (char)(0xD800u | (uint)(w << 6) | (uint)(x >> 10));
		lowSurrogate = (char)(0xDC00u | ((uint)x & 0x3FFu));
	}

	internal static int GetUtf8RepresentationForScalarValue(uint scalar)
	{
		Debug.Assert(scalar <= 1114111);
		if (scalar <= 127)
		{
			return (byte)scalar;
		}
		if (scalar <= 2047)
		{
			byte firstByte2 = (byte)(0xC0u | (scalar >> 6));
			byte secondByteByte = (byte)(0x80u | (scalar & 0x3Fu));
			return (secondByteByte << 8) | firstByte2;
		}
		if (scalar <= 65535)
		{
			byte firstByte3 = (byte)(0xE0u | (scalar >> 12));
			byte secondByte2 = (byte)(0x80u | ((scalar >> 6) & 0x3Fu));
			byte thirdByte2 = (byte)(0x80u | (scalar & 0x3Fu));
			return (((thirdByte2 << 8) | secondByte2) << 8) | firstByte3;
		}
		byte firstByte = (byte)(0xF0u | (scalar >> 18));
		byte secondByte = (byte)(0x80u | ((scalar >> 12) & 0x3Fu));
		byte thirdByte = (byte)(0x80u | ((scalar >> 6) & 0x3Fu));
		byte fourthByte = (byte)(0x80u | (scalar & 0x3Fu));
		return (((((fourthByte << 8) | thirdByte) << 8) | secondByte) << 8) | firstByte;
	}

	[MethodImpl(MethodImplOptionsEx.AggressiveInlining)]
	internal static bool IsCharacterDefined(char c)
	{
		int index = c >>> 5;
		int offset = c & 0x1F;
		return ((GetDefinedCharacterBitmap()[index] >> offset) & 1) != 0;
	}

	[MethodImpl(MethodImplOptionsEx.AggressiveInlining)]
	internal static bool IsSupplementaryCodePoint(int scalar)
	{
		return (scalar & -65536) != 0;
	}

	[MethodImpl(MethodImplOptionsEx.AggressiveInlining)]
	internal static bool IsUtf8ContinuationByte(in byte value)
	{
		return (sbyte)value < -64;
	}
