	public static OperationStatus DecodeScalarValueFromUtf8(ReadOnlySpan<byte> source, out uint result, out int bytesConsumed)
	{
		int index = 0;
		if ((uint)index >= (uint)source.Length)
		{
			goto IL_025a;
		}
		uint tempValue = source[index];
		if (UnicodeUtility.IsAsciiCodePoint(tempValue))
		{
			goto IL_0036;
		}
		if (UnicodeUtility.IsInRangeInclusive(tempValue, 194u, 244u))
		{
			tempValue = tempValue - 194 << 6;
			index++;
			if ((uint)index >= (uint)source.Length)
			{
				goto IL_025a;
			}
			int thisByteSignExtended = (sbyte)source[index];
			if (thisByteSignExtended < -64)
			{
				tempValue += (uint)thisByteSignExtended;
				tempValue += 128;
				tempValue += 128;
				if (tempValue < 2048)
				{
					Debug.Assert(UnicodeUtility.IsInRangeInclusive(tempValue, 128u, 2047u));
					goto IL_0036;
				}
				if (UnicodeUtility.IsInRangeInclusive(tempValue, 2080u, 3343u) && !UnicodeUtility.IsInRangeInclusive(tempValue, 2912u, 2943u) && !UnicodeUtility.IsInRangeInclusive(tempValue, 3072u, 3087u))
				{
					index++;
