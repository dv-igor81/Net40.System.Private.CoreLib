						tempValue += 128;
						tempValue -= 131072;
						if (tempValue <= 65535)
						{
							Debug.Assert(UnicodeUtility.IsInRangeInclusive(tempValue, 2048u, 65535u));
						}
						else
						{
							index++;
							if ((uint)index >= (uint)source.Length)
